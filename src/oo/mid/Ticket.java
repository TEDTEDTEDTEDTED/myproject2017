package oo.mid;
public class Ticket {
    int id;
    String name;
    float price;
    public void setid(int id){
    	this.id=id;
    }
    public void setname(String name){
    	this.name=name;
    }
    public void setprice(float price){
    	this.price=price;
    }
	public int getid(){
		return id;
	}
	public String getname(){
		return name;
	}
	public float getprice(){
		return price;
	}
	
}
